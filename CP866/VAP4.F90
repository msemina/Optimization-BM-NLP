! TECTPOBAHE METOOB  AKETA M METO - (AP4)
PROGRAM VAP4
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB. EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA (HE COETC)
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC)
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /AP41/ X1
    COMMON /AP42/ X2
    COMMON /AP43/ ST
    COMMON /AP44/ SS
    COMMON /AP45/ SF
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1 PABHA N
    REAL(8),DIMENSION(2)::X,A,B,G1
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT BCEX MACCBOB = N
    REAL(8),DIMENSION(2)::X1,X2,ST
    INTEGER,DIMENSION(2)::SS,SF
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=-1.0
    X(2)=-1.0
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-10.
    A(2)=-10.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=10.
    B(2)=10.
! AAHE APAMETPOB METOA
    Q=0
    PAR(Q+1)=0.00001 ! TOHOCT PEEH AA
    PAR(Q+2)=100.    ! MAKCMAHOE CO AOB,KOTOPOE MOHO CEAT
    PAR(Q+3)=0       ! BXOHO APAMETP
    PAR(Q+4)=0.01    ! HAAH A CCKA
    PAR(Q+5)=0.125   ! KOEHT MEHEH AA
    PAR(Q+6)=0.00001 ! "HEOPEEEHHOCT" AAH PAH APAEEEA
    PAR(Q+ 7)=1      ! CO AHX AOB,EPE KOTOPOE  CEET BBOT HOPMA
    PAR( Q+8)=3      ! CTEEH OPOHOCT BBOMO HOPMA: OT 0 O 3
!  
    CALL AP4(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VAP4
