! COOBAHE METOOB  AKETA OTMA H METO HTOHA (C8)
PROGRAM VC8
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 20)
    ! UNCONS - M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/   M1,N,L
    COMMON /A5/   FUNC
    COMMON /A8/   Y1
    COMMON /A9/   Y2
    COMMON /A10/  NF
    COMMON /A13/  Y11
    COMMON /C80/  ZNFPR
    COMMON /C81/  ACTIV
    COMMON /C82/  XPR
    COMMON /C83/  GR
    COMMON /C84/  HES
    COMMON /C85/  LZ
    COMMON /C86/  LZZ
    COMMON /C87/  NAPR
    COMMON /C88/  DVOY
    COMMON /C89/  DVPR
    COMMON /C801/ LRAB
    COMMON /C802/ MRAB
    ! PAMEPHOCT MACCBOB X,A,B  PABHA  N
    REAL(8),DIMENSION(2)::X,A,B
    ! PAMEPHOCT MACCBA  P   PABHA  M
    REAL(8),DIMENSION(4)::P
    ! PAMEPHOCT MACCBA  Y   PABHA  M1=M+1
    REAL(8),DIMENSION(4)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT  MACCBOB GR,XPR  = N
    REAL(8),DIMENSION(2)::XPR,GR
    ! PAMEPHOCT  MACCBOB DVOY,DVPR = M
    REAL(8),DIMENSION(3)::DVOY,DVPR
    ! PAMEPHOCT  MACCBA HES = ( N,N )
    REAL(8),DIMENSION(2,2)::HES
    ! PAMEPHOCT  MACCBA LZZ = ( N+M,N+M )
    REAL(8),DIMENSION(7,7)::LZZ
    ! PAMEPHOCT  MACCBOB LZ,NAPR,LRAB,MRAB = ( N+M )
    REAL(8),DIMENSION(7)::LZ,NAPR,LRAB,MRAB
    ! PAMEPHOCT  MACCBOB Y1,Y2,Y11,FUNC,ZNFPR,ACTIV  = M+1
    REAL(8),DIMENSION(4)::FUNC,Y1,Y2,Y11,ZNFPR
    REAL(8)::F
    INTEGER,DIMENSION(4)::ACTIV
    INTEGER::N,L,M1,Q,M,NF
    EXTERNAL F,CGR,CGS,UNCONS
!
      NF=0
      Q=20
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.1
    X(2)=0.7
    X(3)=0.2
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)= -100000.
    A(2)= -100000.
    A(3)= -100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
    B(3)=100000.
! HAEH BOCTBEHHX EPEMEHHX
    P(1)=1.D0
    P(2)=1.D0
    P(3)=0.D0
    P(4)=0.D0
    P(5)=0.D0
! AAHE APAMETPOB METOA
    PAR(1)=0.0001  ! TOHOCT PEEH AA O HOPME PAEHTA HK APAHA
    PAR(2)=15      ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0       ! AKTECK CEAHHOE CO TEPA
    PAR(4)=0.8     ! APAMETP BOPA AA BEH ( MAOPAHTA OCTEHA )
    PAR(5)=0.01    ! APAMETP BEEH AKTBHX OPAHEH
    PAR(6)=0.1     ! HAEHE,PCBABAEMOE BOCTBEHHM EPEMEHHM, COOTBETCTBM OPAHEHM TA HEPABEHCTBA, HAAHE HAEH KOTOPX MEHE 10** ( -18 )
    PAR(7)=0.1     ! MHMAHOE HAEHE BOCTBEHHO EPEMEHHO, P KOTOPOM OPAHEHE TA HEPABEHCTBA EE CTAETC AKTBHM
    PAR(8)=0.0001  ! A CEHHOO BCEH PAEHTA
    PAR(9)=2       ! HOMEP PAHOCTHO CXEM CEHHOO BCEH PAEHTA ( = 1  2 )
    PAR(10)=0.0001 ! A CEHHOO BCEH ECCAHA
    PAR(11)=1      ! HOMEP PAHOCTHO CXEM CEHHOO BCEH ECCAHA  ( = 1,2  3 )
    PAR(12)=1      ! CO AOB,EPE KOTOPOE  CEET BBOT HOPMA
    PAR(13)=4      ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
!  
    CALL C8(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q,UNCONS)
END PROGRAM VC8
