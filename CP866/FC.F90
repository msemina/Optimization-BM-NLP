SUBROUTINE F(X,Y,J)
!       PMEP OOTOBK OPOPAMM PACETA EEBO
!    HK  OPAHEH  AA COBHO OTMA
!  ( BAPAHT E BHOO ETA APAEEEHX OPAHEH )
!   N,M1 - OPEET PAMEPHOCT AA
    COMMON /A1/ M1,N
!   NF - CETK BCEH HK  OPAHEH
    COMMON /A10/ NF
!   X - BEKTOP BAPPEMX EPEMEHHX
!   Y - BEKTOP HAEH KPTEP  OPAHEH
    INTEGER::M1,N,NF,J
    REAL(8),DIMENSION(N)::X
    REAL(8),DIMENSION(M1)::Y
!   EC J < 0,TO BCEM  EEB HK  BCE OPAHEH
!   EC J = 0,TO BCEM  EEB HK
!   EC J > 0,TO BCEM  J-OE  OPAHEHE
    IF(J==0.OR.J==-1)THEN
        Y(M1)=100 * (X(2) - X(1)**2 )**2 + (1 - X(1))**2
        NF=NF+1
    END IF
    IF(J==1.OR.J==-1)THEN
        Y(1)=(X(1)-1.D0)**2 + (X(2)-1.D0)**2 - 4
        NF=NF+1
    END IF
    IF(J==2.OR.J==-1)THEN
        Y(2)=-X(1)
        NF=NF+1
    END IF
    IF(J==3.OR.J==-1)THEN
        Y(3)=-X(2)
        NF=NF+1
    END IF
!
    RETURN
END SUBROUTINE F
