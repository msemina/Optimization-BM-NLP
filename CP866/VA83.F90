! TECTPOBAHE METOOB  AKETA M METO HTOHA (AOPTM PKOBA A..) (A83)
PROGRAM VA83
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA (COETC CBO BCTPOEHHA POPAMMA BCEH PAEHTA)
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC)
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A830/ M1
    COMMON /A831/ HESP
    COMMON /A832/ G2
    COMMON /A833/ P1
    COMMON /A834/ P2
    COMMON /A835/ XV
    COMMON /A836/ FV
    COMMON /A837/ XT
    COMMON /A838/ TR
    COMMON /A839/ L1
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1 PABHA N
    REAL(8),DIMENSION(2)::X,A,B,G1
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT BCEX MACCBOB = N
    REAL(8),DIMENSION(2)::HESP,G2,P1,P2,XV,FV,XT,TR,L1,M1
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=-1.0
    X(2)=-1.0
! AAHE APAMETPOB METOA
    Q=0
    PAR(Q+1)=0.0001  ! TOHOCT PEEH AA O HOPME PAEHTA
    PAR(Q+2)=20      ! MAKCMAHOE CO AOB, KOTOPOE MOHO CEAT
    PAR(Q+3)=0       ! BXOHO APAMETP
    PAR(Q+4)=0       ! PHAK HAAHO OPEEEHHOCT APAMETPOB Y  G1 ( 0,1  2 )
    PAR(Q+5)=0.001   ! HAAH A CCKA BO PAEHTA
    PAR(Q+6)=0.49    ! MAOPAHTA OCTEHA ( B HTEPBAE [0.000001;0.499999] )
    PAR(Q+7)=0       ! KOECTBO TEPA  OCTPOEH TEKEO HAPABEH OCKA KCTPEMMA; EC =0 ,TO EAETC N TEPA
    PAR(Q+8)=2       ! OPOK EPEHPOBAH ( = 1  2 )
    PAR(Q+9)=0.00001 ! A KOHEHO-PAHOCTHO APOKCMA POBOHX
    PAR(Q+10)=1      ! CO AHX AOB, EPE KOTOPOE  CEET BBOT HOPMA
    PAR(Q+11)=3      ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 3 )
!  
    CALL A83(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VA83
