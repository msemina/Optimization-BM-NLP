         REAL *8  P1( 2 ),XV( 2 ),L1( 2 ),M1( 2 ),
     *           FV( 2 ),XT( 2 ),TR( 2 )
C
C           XAPAKTEPCTKA APAMETPOB POPAMM METOA
C
C     N - PAMEPHOCT AA
