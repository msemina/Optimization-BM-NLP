         PAR(Q+6)=0.45
C     A KOHEHO-PAHOCTHO APOKCMA POBOHX
         PAR(Q+7)=0.00001
C     CO AHX AOB,EPE KOTOPOE  CEET BBOT
C     HOPMA
         PAR(Q+8)=1
C     CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 3 )
         PAR(Q+9)=3
C
         CALL A8 (N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
         END
