! COOBAHE METOOB  AKETA OTMA H METO  (C9)
PROGRAM VC9
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 20)
    ! UNCONS -  M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/  M1,N,L
    COMMON /A2/  XN
    COMMON /A3/  GN
    COMMON /A4/  G
    COMMON /A5/  G2
    COMMON /A6/  BG
    COMMON /A7/  Y1
    COMMON /A8/  Y11
    COMMON /A9/  Y12
    COMMON /A10/ NF
    REAL(8)::F
    INTEGER::N,L,M1,Q,M,NF
    ! PAMEPHOCT MACCBOB X,A,B PABHA N
    REAL(8),DIMENSION(3)::X,A,B
    ! PAMEPHOCT MACCBA P PABHA M
    REAL(8),DIMENSION(5)::P
    ! PAMEPHOCT MACCBA Y PABHA M1=M+1
    REAL(8),DIMENSION(6)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT MACCBOB GN,XN,G,G2 = N
    REAl(8),DIMENSION(3)::XN,GN,G,G2
    ! PAMEPHOCT MACCBOB Y1,Y12,Y11 = M+1
    REAl(8),DIMENSION(6)::Y1,Y11,Y12
    ! PAMEPHOCT MACCBA BG = (N,N)
    REAl(8),DIMENSION(9)::BG
    EXTERNAL F,CGR,CGS
!
    NF=0
    Q=20
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.1
    X(2)=0.7
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-100000.
    A(2)=-100000.
    A(3)=-100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
    B(3)=100000.
! HAEH BOCTBEHHX EPEMEHHX
         P(1)=1.D0
         P(2)=1.D0
         P(3)=0.D0
         P(4)=0.D0
         P(5)=0.D0
! AAHE APAMETPOB METOA
    ! TOHOCT PEEH AA O OEM COA,
    PAR(1)=0.00000000001 ! OKAEO PEEHE
    PAR(2)=150           ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0.            ! AKTECK CEAHHOE CO TEPA
    PAR(4)=1.            ! PAC HAAHOO APA, COEPAEO PEEHE
    PAR(5)=1.5           ! BEC TPAA
    PAR(6)=0.001         ! APAMETP PEOPAOBAH OPAHEH TA PABEHCTBA B HEPABEHCTBA
    PAR(7)=0.00001       ! A CEHHOO BCEH PAEHTA
    PAR(8)=1.            ! HOMEP PAHOCTHO CXEM CEHHOO BCEH PAEHTA ( = 1  2 )
    PAR(9)=1.            ! CO AOB, EPE KOTOPOE  CEET BBOT HOPMA
    PAR(10)=4.           ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
!  
    CALL C9(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q)
END PROGRAM VC9
