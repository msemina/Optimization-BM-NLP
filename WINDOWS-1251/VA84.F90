PROGRAM VA84
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC )
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A841/ HES
    COMMON /A842/ P1
    COMMON /A843/ P2
    COMMON /A844/ DGR
    COMMON /A845/ XV
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1  PABHA  N
    REAL(8),DIMENSION(40)::PAR
    REAL(8),DIMENSION(2)::X,A,B,G1
    ! PAMEPHOCT  MACCBA HES  = N*N
    REAL(8),DIMENSION(2,2)::HES
    ! PAMEPHOCT BCEX MACCBOB  = N
    REAL(8),DIMENSION(2)::P1,P2,DGR ,XV
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=-1.0
    X(2)=-1.0
! AAHE APAMETPOB METOA
    Q=0
    PAR(Q+1)=0.0001   ! TOHOCT PEEH AA O HOPME PAEHTA
    PAR(Q+2)=20       ! MAKCMAHOE CO AOB, KOTOPOE MOHO CEAT
    PAR(Q+3)=0        ! BXOHO APAMETP
    PAR(Q+4)=0        ! PHAK HAAHO OPEEEHHOCT APAMETPOB Y  G1 ( 0,1  2 )
    PAR(Q+5)=0.001    ! HAAH A CCKA
    PAR(Q+6)=0.45     ! MAOPAHTA OCTEHA
    PAR(Q+7)=4        ! HOMEP BEPC METOA ( 1,2,3,  4 )
    PAR(Q+8)=0        ! PHAK COCOA BCEH HAAHO MATP HAPABEH  ( 0  1 )
    PAR(Q+9)=2        ! OPOK EPEHPOBAH ( = 1  2 )
    PAR(Q+10)=0.00001 ! A EPEHPOBAH
    PAR(Q+11)=1       ! PHAK CXEM BCEH ECCAHA, EC PAR(Q+8)=1;    ( 1,2  3 )
    PAR(Q+12)=1       ! CO AHX AOB, EPE KOTOPOE CEET BBOT HOPMA
    PAR(Q+13)=3       ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 3 )
!  
         CALL A84(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VA84
