! COOBAHE METOOB  AKETA OTMA      (CP5)
! ( OBETBOPEH OPAHEH COETC METO ECOBHO MHMA AP3)
PROGRAM VCP5
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 20)
    ! AP3 - M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/     M1,N,L
    COMMON /A8/     Y11
    COMMON /A9/     Y2
    COMMON /A11/    G
    COMMON /A10/    NF
    COMMON /A13/    Y1
    COMMON /AP31/   P1
    COMMON /AP32/   P2
    COMMON /AP33/   G2
    COMMON /AP34/   XV
    COMMON /AP35/   W
    COMMON /AP36/   WN
    COMMON /AGR20/  Y4
    COMMON /AGR30/  PT
    COMMON /AGR40/  GB
    COMMON /AGES10/ G1
    COMMON /AGES20/ GBT
    REAL(8)::F
    INTEGER::N,M,M1,L,Q,NF
    ! PAMEPHOCT MACCBOB X,A,B PABHA N
    REAL(8),DIMENSION(3)::X,A,B
    ! PAMEPHOCT MACCBA P PABHA M
    REAL(8),DIMENSION(5)::P
    ! PAMEPHOCT MACCBA Y PABHA M1=M+1
    REAL(8),DIMENSION(6)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT MACCBOB G,GB,G1 = N
    REAL(8),DIMENSION(3)::G,GB,G1
    ! PAMEPHOCT MACCBA GBT = (N,N)
    REAL(8),DIMENSION(3,3)::GBT
    ! PAMEPHOCT MACCBOB Y4,Y1,Y2,Y11 = M+1
    REAL(8),DIMENSION(6)::Y11,Y2,Y1,Y4
    ! PAMEPHOCT MACCBA  PT = M
    REAL(8),DIMENSION(5)::PT
    ! EPEMEHHE  METOA ECOBHO OTMA (AP3)
    ! PAMEPHOCT MACCBOB P1,P2,G2,XV,W,WN = max{N,M}
    REAL(8),DIMENSION(6)::P1,P2,G2,XV
    INTEGER,DIMENSION(6)::W,WN
    EXTERNAL F,CGR,CGS,AP3
! 
    NF=0
    Q=20
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.1
    X(2)=0.7
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-100000.
    A(2)=-100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
! HAEH BOCTBEHHX EPEMEHHX
    P(1)=1.D0
    P(2)=1.D0
    P(3)=0.D0
    P(4)=0.D0
    P(5)=0.D0
! AAHE APAMETPOB METOA
    PAR(1)=0.0001       ! TOHOCT PEEH AA
    PAR(2)=50.          ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0.           ! AKTECK CEAHHOE CO TEPA
    PAR(4)=2.           ! HOMEP BEPC METOA ( = 1,2  3 )
    PAR(5)= 0.1         ! BEC OPAHEH B COB OCTAHOBA
    PAR(6)=10.          ! APAMETP PEPA
    PAR(7)= 0.5         ! KOEHT MEHEH TOHOCT PEEH AA M
    PAR(8)=0.02         ! APAMETP PABEH HKE TPAA
    PAR(9)=1.           ! CO AOB,EPE KOTOPOE CEET BBOT HOPMA
    PAR(10)=4.          ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
    ! APAMETP METOA COPEHHX PAEHTOB (AP3)
    PAR(Q+1)=0.1        ! TOHOCT PEEH AA O HOPME PAEHTA
    PAR(Q+2)=10.        ! MAKCMAHOE CO AOB, KOTOPOE MOHO CEAT
    PAR(Q+3)=0.         ! BXOHO APAMETP
    PAR(Q+4)=0.0001     ! HAAH A CCKA
    PAR(Q+5)=0.1        ! HAAHA TOHOCT PEEH OHOMEPHO AA BOPA OTMAHOO AA
    PAR(Q+6)=0.00000001 ! MHMAH A CCKA
    PAR(Q+7)=10.        ! CO TEPA, EPE KOTOPOE METO "BOCCTAHABBAETC"
    PAR(Q+8)=1.         ! HOMEP BEPC METOA ( = 1  2 )
    PAR(Q+9)=1.         ! OPOK EPEHPOBAH ( = 1  2 )
    PAR(Q+10)=0.0001    ! A EPEHPOBAH
    PAR(Q+11)=1.        ! CO AHX AOB, EPE KOTOPOE  CEET BBOT HOPMA
    PAR(Q+12)=0.        ! CTEEH OPOHOCT BBOMO HOPMA: OT 0 O 3
!  
    CALL CP5(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q,AP3)
END PROGRAM VCP5
