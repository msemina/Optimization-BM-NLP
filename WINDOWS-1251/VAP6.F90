! TECTPOBAHE METOOB  AKETA M METO C  (AP6)
PROGRAM VAP6
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA (HE COETC)
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC)
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /AP61/ Z1
    COMMON /AP62/ XM
    COMMON /AP63/ W
    COMMON /AP64/ WM
    COMMON /AP65/ X1
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1  PABHA  N
    REAL(8),DIMENSION(2)::X,A,B,G1
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT BCEX MACCBOB = N
    REAL(8),DIMENSION(2)::Z1,XM,W,WM,X1
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=-1.0
    X(2)=-1.0
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-10.
    A(2)=-10.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=10.
    B(2)=10.
! AAHE APAMETPOB METOA
    Q=0
    PAR(1)=0.0001   ! TOHOCT PEEH AA
    PAR(2)=500      ! MAKCMAHOE CO AOB,KOTOPOE MOHO CEAT
    PAR(3)=0        ! BXOHO APAMETP
    PAR(4)=0.1      ! HAAH A CCKA
    PAR(5)=1.5      ! KOEHT BEEH AA CCKA
    PAR(6)=0.903602 ! KOEHT MEHEH AA CCKA
    PAR(7)=5.0      ! APAMETP MEHEH OTHOCT PACPEEEH
    PAR(8)=10       ! CO AHX AOB,EPE KOTOPOE MEHETC OTHOCT PACPEEEH CAHX CE
    PAR(9) =20      ! OEE CO AOB,EPE KOTOPOE MEHETC OTHOCT PACPEEEH CAHX CE
    PAR(10)= 13579  ! HEETHOE CO  HTEPBAA (1;67108863)  ACKA EHEPATOPA CAHX CE
    PAR(11)=1       ! CO AHX AOB,EPE KOTOPOE  CEET BBOT HOPMA
    PAR(12)=3       ! CTEEH OPOHOCT BBOMO HOPMA: OT 0 O 3
!  
    CALL AP6(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VAP6
