! COOBAHE METOOB  AKETA OTMA H METO  (C2)
PROGRAM VC2
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM (= 20)
    ! UNCONS -  M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/  M1,N,L
    COMMON /A2/  GR
    COMMON /A3/  GRF
    COMMON /A4/  HX
    COMMON /A5/  XQ
    COMMON /A6/  U
    COMMON /A7/  YQ
    COMMON /A8/  Y1
    COMMON /A9/  Y2
    COMMON /A11/ ES
    COMMON /A10/ NF
    COMMON /A12/ IS
    COMMON /A13/ IT
    COMMON /A20/ SM
    COMMON /A21/ SN
    COMMON /A22/ U1
    COMMON /A23/ Y11
    INTEGER::N,L,M1,Q,M,VAR,NF
    REAL(8)::F
    ! PAMEPHOCT MACCBOB X,A,B PABHA N
    REAL(8),DIMENSION(3)::X,A,B
    ! PAMEPHOCT MACCBA P PABHA M
    REAL(8),DIMENSION(5)::P
    ! PAMEPHOCT MACCBA Y PABHA M1=M+1
    REAL(8),DIMENSION(6)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT MACCBOB GR,GRF,HX,XQ,IT = N
    REAL(8),DIMENSION(3)::GR,GRF,HX,XQ
    INTEGER,DIMENSION(3)::IT
    ! PAMEPHOCT MACCBOBA U,IS = M
    REAL(8),DIMENSION(5)::U
    INTEGER,DIMENSION(5)::IS
    ! PAMEPHOCT MACCBOB YQ,Y1,Y2 = M1
    REAL(8),DIMENSION(6)::YQ,Y1,Y2
    ! PAMEPHOCT MACCBOB Y11,SN = ( MAX(N+1,M1) )
    REAL(8),DIMENSION(6)::Y11
    INTEGER,DIMENSION(6)::SN
    ! PAMEPHOCT MACCBA ES = ( MAX(N+2,M+2),2N+M+L+1 )
    REAL(8),DIMENSION(7,13)::ES
    ! PAMEPHOCT MACCBOB U1,SM = ( MAX(2N+1,M+L+2N) )
    REAL(8),DIMENSION(12)::U1
    INTEGER,DIMENSION(12)::SM
    EXTERNAL F,CGR,CGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.5
    X(2)=0.7
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-100000.
    A(2)=-100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
    !  AHHOO METOA  MACCB A  B HE COTC
! HAEH BOCTBEHHX EPEMEHHX
    P(1)=1.D0
    P(2)=1.D0
    P(3)=0.D0
    P(4)=0.D0
    P(5)=0.D0
! AAHE APAMETPOB METOA
    NF=0
    Q=20
    PAR(1)=0.0001 ! TOHOCT PEEH AA
    PAR(2)=1000   ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0      ! AKTECK CEAHHOE CO TEPA
    PAR(4)=1      ! HOMEP COCOA PEEH HEHX AA ( = 1  2 )
    PAR(5)=0.1    ! BEC OPAHEH B COB OCTAHOBA METOA ( > 0 )
    PAR(6)=1.     ! HAAH A CCKA ( > 0 )
    PAR(7)=10.    ! HAAHOE HAEHE KOEHTA TPAA ( > 0 )
    PAR(8)=0.05   ! HAAHOE HAEHE OCTMOO HAPEH OPAHEH ( > 0 )
    PAR(9)=0.0001 ! A CEHHOO EPEHPOBAH
    PAR(10)=1     ! HOMEP CXEM CEHHOO EPEHPOBAH ( = 1  2 )
    PAR(11)=1     ! CO AOB,EPE KOTOPOE CEET BBOT HOPMA
    PAR(12)=4     ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
!
    CALL C2(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q)
END PROGRAM VC2
