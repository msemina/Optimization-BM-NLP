! TECTPOBAHE METOOB  AKETA M METO   (AP1)
PROGRAM VAP1
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC)
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /AP11/ X1
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1 PABHA N
    REAL(8),DIMENSION(2)::X,A,B,G1
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT  MACCBA X1 = N
    REAL(8),DIMENSION(2)::X1
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=0.
    X(2)=0.
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-10.
    A(2)=-10.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=10.0
    B(2)=10.0
! AAHE APAMETPOB METOA
    Q=0 
    PAR(Q+1)=0.000000001 ! TOHOCT PEEH AA O MEHEH HKOHAA
    PAR(Q+2)=5000        ! MAKCMAHOE CO AOB, KOTOPOE MOHO CEAT
    PAR(Q+3)=0           ! BXOHO APAMETP
    PAR(Q+4)=1.0         ! HAAH A CCKA
    PAR(Q+5)=0.6         ! MAOPAHTA APMO
    PAR(Q+6)=0.5         ! KOEHT MEHEH AA
    PAR(Q+7)=0.45        ! MAOPAHTA OCTEHA
    PAR(Q+8)=6           ! HOMEP BEPC METOA ( = 1,2,3,4,5  6 )
    PAR(Q+9)=0.00001     ! "HEOPEEEHHOCT" AAH PAH APAEEEA
    PAR(Q+10)=1          ! OPOK EPEHPOBAH ( = 1  2 )
    PAR(Q+11)=0.00001    ! A EPEHPOBAH
    PAR(Q+12)=1          ! CO AHX AOB, EPE KOTOPOE  CEET BBOT HOPMA
    PAR(Q+13)=3          ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 3 )
!  
    CALL AP1(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VAP1
