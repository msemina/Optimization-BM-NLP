! METO   (CP1)
PROGRAM VCP1
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 20)
    ! UNCONS -  M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/  M1,N,L
    COMMON /A2/  IS
    COMMON /A3/  XA
    COMMON /A4/  HX
    COMMON /A5/  D1
    COMMON /A6/  D2
    COMMON /A7/  GR
    COMMON /A8/  Y1
    COMMON /A9/  Y2
    COMMON /A10/ NF
    COMMON /A11/ ES
    COMMON /A12/ A1
    COMMON /A13/ A2
    COMMON /A14/ YA
    COMMON /A20/ SM
    COMMON /A21/ SN
    COMMON /A22/ U1
    COMMON /A23/ Y11
    REAL(8)::F
    INTEGER::N,L,M1,Q,M,NF
    ! PAMEPHOCT MACCBOB X,A,B PABHA N
    REAL(8),DIMENSION(2)::X,A,B
    ! PAMEPHOCT MACCBA P PABHA M
    REAL(8),DIMENSION(3)::P
    ! PAMEPHOCT MACCBA Y PABHA M1=M+1
    REAL(8),DIMENSION(4)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT MACCBOB XA,GR,HX,D1,D2 = N
    REAL(8),DIMENSION(2)::XA,HX,D1,D2,GR
    ! PAMEPHOCT MACCBOB U1,SM = N+1
    REAL(8),DIMENSION(3)::U1
    INTEGER,DIMENSION(4)::SM
    ! PAMEPHOCT MACCBOB YA,Y1,Y2 = M+1
    REAL(8),DIMENSION(4)::YA,Y1,Y2
    ! PAMEPHOCT MACCBA ES = ( N+M+2,N+2 )
    REAL(8),DIMENSION(10,5)::ES
    ! PAMEPHOCT MACCBA A1 = ( N+1,N+2 )
    REAL(8),DIMENSION(4,5)::A1
    ! PAMEPHOCT MACCBA A2 = ( L+1,N+2 )
    REAL(8),DIMENSION(2,5)::A2
    ! PAMEPHOCT MACCBOB Y11,SN = N+M+1
    REAL(8),DIMENSION(9)::Y11
    INTEGER,DIMENSION(9)::SN
    ! PAMEPHOCT MACCBA IS = ( M+2 )
    INTEGER,DIMENSION(7)::IS
    EXTERNAL F,CGR,CGS
! 
    NF=0
    Q=20
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.3
    X(2)=0.7
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-100000.
    A(2)=-100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
! HAEH BOCTBEHHX EPEMEHHX
    P(1)=1.D0
    P(2)=1.D0
    P(3)=0.D0
! AAHE APAMETPOB METOA
    PAR(1)=0.0001 ! TOHOCT PEEH AA
    PAR(2)=110.   ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0.     ! AKTECK CEAHHOE CO TEPA
    PAR(4)=1.     ! HOMEP BEPC METOA ( = 1  2 )
    PAR(5)=0.1    ! BE OPAHEH B COB OCTAHOBA METOA ( > 0 )
    PAR(6)=5.     ! APAMETP BH EEBO HK HA BOP HAPABEH BO BCOMOATEHX AAAX
    PAR(7)=1.     ! HAAH A CCKA ( > 0 )
    PAR(8)=0.1    ! HAAHOE HAEHE OCTMOO HAPEH OPAHEH ( > 0 )
    PAR(9)=0.0001 ! A CEHHOO EPEHPOBAH
    PAR(10)=1.    ! HOMEP CXEM CEHHOO EPEHPOBAH ( = 1  2 )
    PAR(11)=1.    ! CO AOB, EPE KOTOPOE CEET BBOT HOPMA
    PAR(12)=4.    ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
!  
    CALL CP1(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q)
END PROGRAM VCP1
