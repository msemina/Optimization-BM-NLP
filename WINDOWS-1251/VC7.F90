! COOBAHE METOOB  AKETA OTMA H PE  (C7)
PROGRAM VC7
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT BEKTOPA PABEMX EPEMEHHX
    ! L - CO OPAHEH TA PABEHCTB
    ! M - OEE CO OPAHEH
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! P - BEKTOP BOCTBEHHX EPEMEHHX
    ! F - M OPOPAMM TA SUBROUTINE  BCEH HAEH KPTEP  OPAHEH
    ! CGR - M OPOPAMM TA SUBROUTINE  BCEH PAEHTOB EEBO HK  OPAHEH
    ! CGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX EEBO HK  OPAHEH
    ! Y - BEKTOP HAEH KPTEP  OPAHEH
    ! PAR - BEKTOP APAMETPOB METOA
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 20)
    ! UNCONS - M OPOPAMM METOA ECOBHO MHMA
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /A1/  M1,N,L
    COMMON /A2/  GF
    COMMON /A3/  GFT
    COMMON /A4/  AA
    COMMON /A5/  GR
    COMMON /A6/  X1
    COMMON /A7/  X2
    COMMON /A8/  Y11
    COMMON /A9/  Y12
    COMMON /A10/ NF
    COMMON /A11/ BB
    COMMON /A12/ Y1
    COMMON /A13/ IPVT
    COMMON /A14/ WORK
    REAL(8)::F
    INTEGER::N,L,M1,Q,M,NF
    ! PAMEPHOCT MACCBOB X,A,B  PABHA  N
    REAL(8),DIMENSION(3)::X,A,B
    ! PAMEPHOCT MACCBA P PABHA M
    REAL(8),DIMENSION(5)::P
    ! PAMEPHOCT MACCBA Y PABHA M1=M+1
    REAL(8),DIMENSION(6)::Y
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT  MACCBOB GR,X1,X2,IPVT  = N
    REAL(8),DIMENSION(3)::GR,X1,X2
    INTEGER,DIMENSION(3)::IPVT
    ! PAMEPHOCT  MACCBOB BB,WORK  = M
    REAL(8),DIMENSION(5)::BB,WORK
    ! PAMEPHOCT  MACCBOB Y11,Y1,Y12  = M+1
    REAL(8),DIMENSION(6)::Y1,Y11,Y12
    ! PAMEPHOCT  MACCBA AA  = ( M,M )
    REAL(8),DIMENSION(5,5)::AA
    ! PAMEPHOCT  MACCBA GF  = ( N,M )
    REAL(8),DIMENSION(3,5)::GF
    ! PAMEPHOCT  MACCBOB GFT = ( M,N )
    REAL(8),DIMENSION(5,3)::GFT
    EXTERNAL F,CGR,CGS
! 
    NF=0
    Q=20
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    M1=4
    M=M1-1
    L=0
    N=2
    ! HAAHA TOKA
    X(1)=0.3
    X(2)=0.7
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-100000.
    A(2)=-100000.
    A(3)=-100000.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=100000.
    B(2)=100000.
    B(3)=100000.
! HAEH BOCTBEHHX EPEMEHHX
    P(1)=1.D0
    P(2)=1.D0
    P(3)=0.D0
    P(4)=0.D0
    P(5)=0.D0
! AAHE APAMETPOB METOA
    PAR(1)=0.0001 ! TOHOCT PEEH AA
    PAR(2)=1000   ! MAKCMAHO BOMOHOE CO TEPA
    PAR(3)=0      ! AKTECK CEAHHOE CO TEPA
    PAR(4)=1.     ! HAAH A CCKA ( > 0 )
    PAR(5)=1      ! HOMEP CXEM CEHHOO EPEHPOBAH ( = 1  2 )
    PAR(6)=0.0001 ! A CEHHOO EPEHPOBAH
    PAR(7)=1      ! CO AOB,EPE KOTOPOE  CEET BBOT HOPMA
    PAR(8)=4      ! CTEEH OPOHOCT BBOMO HOPMA ( OT 0 O 4 )
!  
    CALL C7(N,L,M,X,A,B,P,F,CGR,CGS,Y,PAR,Q)
END PROGRAM VC7
