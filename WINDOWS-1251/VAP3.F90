! TECTPOBAHE METOOB  AKETA M METO ƨ  (AP3)
PROGRAM VAP3
! XAPAKTEPCTKA APAMETPOB POPAMM METOA
    ! N - PAMEPHOCT AA
    ! X - BEKTOP PABEMX EPEMEHHX
    ! A - BEKTOP EBX PAH MEHEH PAB. EPEMEHHX
    ! B - BEKTOP PABX PAH MEHEH PAB.EPEMEHHX
    ! F - M OPOPAMM TA FUNCTION  BCEH HAEH KPTEP
    ! GRAD - M OPOPAMM TA SUBROUTINE  BCEH PAEHTA
    ! AGS - M OPOPAMM TA SUBROUTINE  PACETA MATP BTOPX POBOHX (HE COETC)
    ! Y - HAEHE KPTEP
    ! G1 - BEKTOP HAEH POBOHX HK F
    ! Q - APAMETP C KCPOBAHHM HAEHEM ( = 0)
    ! PAR - BEKTOP APAMETPOB METOA
    ! FNLP - KCPOBAHHOE M OPOPAMM
! OCAHE APAMETPOB POPAMM METOA
    ! OCAHE OX OACTE METOA
    COMMON /AP31/ P1
    COMMON /AP32/ P2
    COMMON /AP33/ G2
    COMMON /AP34/ XV
    COMMON /AP35/ W
    COMMON /AP36/ WN
    REAL(8)::F,FNLP,Y
    INTEGER::Q
    ! PAMEPHOCT MACCBOB X,A,B,G1 PABHA N
    REAL(8),DIMENSION(2)::X,A,B,G1
    REAL(8),DIMENSION(40)::PAR
    ! PAMEPHOCT BCEX MACCBOB = N
    REAL(8),DIMENSION(2)::P1,P2,G2,XV
    INTEGER,DIMENSION(2)::W,WN
    EXTERNAL F,FNLP,GRAD,AGS
! CXOHE AHHE AA
    ! PAMEPHOCT AA
    N=2
    ! HAAHA TOKA
    X(1)=-1.0
    X(2)=-1.0
    ! EBE PAH PAB. EPEMEHHX O KAO KOOPHATE
    A(1)=-10.
    A(2)=-10.
    ! PABE PAH PAB. EPEMEHHX O KAO KOOPHATE
    B(1)=10.
    B(2)=10.
! AAHE APAMETPOB METOA
    Q=0
    PAR(Q+1)=0.001      ! TOHOCT PEEH AA O HOPME PAEHTA
    PAR(Q+2)=10         ! MAKCMAHOE CO AOB,KOTOPOE MOHO CEAT
    PAR(Q+3)=0          ! BXOHO APAMETP
    PAR(Q+4)=0.0001     ! HAAH A CCKA
    PAR(Q+5)=0.1        ! HAAHA TOHOCT PEEH OHOMEPHO AA BOPA OTMAHOO AA
    PAR(Q+6)=0.00000001 ! MHMAH A CCKA
    PAR(Q+7)=10         ! CO TEPA,EPE KOTOPOE METO "BOCCTAHABBAETC"
    PAR(Q+8)=1          ! HOMEP BEPC METOA ( = 1  2 )
    PAR(Q+9)=2          ! OPOK EPEHPOBAH ( = 1  2 )
    PAR(Q+10)=0.00001   ! A EPEHPOBAH
    PAR(Q+11)=1         ! CO AHX AOB,EPE KOTOPOE  CEET BBOT HOPMA
    PAR(Q+12)=3         ! CTEEH OPOHOCT BBOMO HOPMA: OT 0 O 3
!  
    CALL AP3(N,X,A,B,F,GRAD,AGS,Y,G1,Q,PAR,FNLP)
END PROGRAM VAP3
